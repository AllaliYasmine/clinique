                                                    timingVar      : action,
                                                    timingValue    : timing,
                                                    timingLabel    : label,
                                                    nonInteraction: 1
                                                };
                                                MonsterInsightsObject.sendEvent( fieldsArray );
                                            }
                                        									}

									function calculateMarks(docHeight) {
										return {
